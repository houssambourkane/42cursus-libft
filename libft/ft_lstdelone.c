#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	t_list	*temp;

	if (lst == NULL)
		return ;
	temp = lst;
	del(temp->content);
	lst = temp->next;
	free(temp);
}
